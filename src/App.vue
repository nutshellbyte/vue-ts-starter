<script setup lang="ts">
import { computed } from 'vue';
import { useRoute } from 'vue-router';

import DefaultLayout from '@/layouts/Default.vue';

const route = useRoute();

const layout = computed(() => {
    return route.meta.layout || DefaultLayout;
});
</script>

<template>
    <div id="app">
        <component :is="layout">
            <RouterView />
        </component>
    </div>
</template>
